use std::fmt::Debug;

// Insertion sort
pub fn sort<T: Copy + PartialOrd + Debug>(mut a: Vec<T>) -> Vec<T> {
    let n = a.len();

    for i in 1..n {
        let key = a[i];
        let mut j = i;

        while j > 0 && a[j - 1] > key {
            a[j] = a[j - 1];
            j -= 1;
        }

        a[j] = key;
    }

    a
}

